uint32_t Ultrasonic_GetDistance(void)
{
	uint32_t calculated_time = 0;
	uint32_t distance = 0;
	
	calculated_time = ICU_Capture();
	
	/*the time is returned in microseconds and the distance is calculated in cm*/
	distance = (((uint32_t)(calculated_time * .034)) >> 1);
	
	return distance;
}


